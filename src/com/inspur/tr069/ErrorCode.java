
package com.inspur.tr069;

public class ErrorCode {

    private String ErrorCodeTime;
    private String ErrorCodeValue;

    public String getErrorCodeTime() {
        return ErrorCodeTime;
    }

    public void setErrorCodeTime(String errorCodeTime) {
        ErrorCodeTime = errorCodeTime;
    }

    public String getErrorCodeValue() {
        return ErrorCodeValue;
    }

    public void setErrorCodeValue(String errorCodeValue) {
        ErrorCodeValue = errorCodeValue;
    }
}
